/// INV-D7: numeric rank of a CompletionPhase for monotonic ordering.
/// Used by per-node phase-regression check in `apply_completion_report`.
/// The aggregate `AllocationState` is derived from `per_node_phase`, not
/// compared directly against incoming phases, so state_rank is unused.
fn phase_rank(p: CompletionPhase) -> u8 {
    match p {
        CompletionPhase::Staging => 1,
        CompletionPhase::Running => 2,
        CompletionPhase::Completed => 3,
        CompletionPhase::Failed => 3,
    }
}

/// Metadata about an archived chunk of audit log entries stored in external storage (S3).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct AuditArchive {
    /// S3 object key where the archived entries are stored.
    pub object_key: String,
    /// Number of entries in this archive chunk.
    pub entry_count: usize,
    /// Timestamp of the first entry in the chunk.
    pub first_timestamp: DateTime<Utc>,
    /// Timestamp of the last entry in the chunk.
    pub last_timestamp: DateTime<Utc>,
    /// SHA-256 hash of the last entry in the chunk (for chain continuity).
    pub last_entry_hash: String,
}

fn default_audit_retention() -> usize {
    10_000
}

fn default_signing_key() -> SigningKey {
    SigningKey::generate(&mut rand_core::OsRng)
}

/// The complete cluster state replicated via Raft.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct GlobalState {
    pub nodes: HashMap<NodeId, Node>,
    pub allocations: HashMap<AllocId, Allocation>,
    pub tenants: HashMap<TenantId, Tenant>,
    pub vclusters: HashMap<VClusterId, VCluster>,
    pub topology: TopologyModel,
    pub audit_log: Vec<AuditEntry>,
    /// Maximum number of audit entries to keep in Raft state.
    /// Older entries are archived during compaction. Default: 10,000.
    #[serde(default = "default_audit_retention")]
    pub audit_retention_limit: usize,
    /// Archived audit log summaries — pointers to S3 objects containing older entries.
    /// Each archive chunk covers a time range and stores the last entry's hash for chain continuity.
    #[serde(default)]
    pub audit_archives: Vec<AuditArchive>,
    /// System-wide limit on nodes that can be claimed for sensitive use.
    /// None means unlimited (no sensitive pool cap).
    pub sensitive_pool_size: Option<u32>,
    /// Monotonic version counter. Incremented on every state mutation.
    /// Used by the scheduler for optimistic concurrency control (ADV-04).
    #[serde(default)]
    pub state_version: u64,
    /// Network domains keyed by (tenant, name) for tenant-scoped uniqueness (ADV-09).
    #[serde(default)]
    pub network_domains: HashMap<String, NetworkDomain>,
    /// VNI pool: tracks allocated VNIs for authoritative management (ADV-08).
    #[serde(default)]
    pub vni_pool: VniPool,
    /// Service registry: maps service name → registered endpoints.
    /// Updated automatically when allocations with `expose` endpoints
    /// transition to Running (register) or terminal states (deregister).
    #[serde(default)]
    pub service_registry: HashMap<String, ServiceRegistryEntry>,
    /// Active sessions tracked globally for session limit enforcement (F20).
    #[serde(default)]
    pub sessions: HashMap<SessionId, Session>,
    /// Ed25519 signing key for audit log entries. Not serialized into Raft snapshots —
    /// keys are loaded from site configuration on startup, not replicated via consensus.
    /// A fresh random key is generated by Default for testing; production deployments
    /// must call `set_signing_key()` with the site's persistent key.
    #[serde(skip, default = "default_signing_key")]
    pub audit_signing_key: SigningKey,
}

/// Authoritative VNI pool allocator (ADV-08).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct VniPool {
    /// Start of the VNI range (inclusive).
    pub start: u32,
    /// End of the VNI range (inclusive).
    pub end: u32,
    /// Currently allocated VNIs (value = domain key "tenant/name").
    pub allocated: HashMap<u32, String>,
    /// Next candidate VNI to try (F19: avoids O(n) linear scan).
    #[serde(default)]
    pub next_candidate: u32,
}

impl Default for VniPool {
    fn default() -> Self {
        Self {
            start: 100,
            end: 4095,
            allocated: HashMap::new(),
            next_candidate: 100,
        }
    }
}

impl VniPool {
    /// Allocate the next available VNI. Returns None if pool is exhausted.
    pub fn allocate(&mut self, domain_key: &str) -> Option<u32> {
        // Start from next_candidate for O(1) amortized allocation
        for vni in self.next_candidate..=self.end {
            if let std::collections::hash_map::Entry::Vacant(e) = self.allocated.entry(vni) {
                e.insert(domain_key.to_string());
                self.next_candidate = vni + 1;
                return Some(vni);
            }
        }
        // Wrap around to start
        for vni in self.start..self.next_candidate {
            if let std::collections::hash_map::Entry::Vacant(e) = self.allocated.entry(vni) {
                e.insert(domain_key.to_string());
                self.next_candidate = vni + 1;
                return Some(vni);
            }
        }
        None
    }

    /// Release a VNI back to the pool.
    pub fn release(&mut self, vni: u32) {
        self.allocated.remove(&vni);
    }
}

