    /// Load signing key from a 32-byte seed (from SecretResolver).
    pub fn load_signing_key_from_bytes(&mut self, seed: &[u8; 32]) -> Result<(), String> {
        self.audit_signing_key = SigningKey::from_bytes(seed);
        Ok(())
    }

    /// Load signing key from a 32-byte seed file (F10).
    /// Returns Ok(()) if loaded, Err if file missing or invalid.
    pub fn load_signing_key_from_file(&mut self, path: &std::path::Path) -> Result<(), String> {
        let bytes = std::fs::read(path)
            .map_err(|e| format!("failed to read signing key from {}: {e}", path.display()))?;
        if bytes.len() < 32 {
            return Err(format!(
                "signing key file must be at least 32 bytes, got {}",
                bytes.len()
            ));
        }
        let seed: [u8; 32] = bytes[..32]
            .try_into()
            .map_err(|_| "failed to extract 32-byte seed".to_string())?;
        self.audit_signing_key = SigningKey::from_bytes(&seed);
        Ok(())
    }

    /// Apply a command to the state machine, returning a response.
    pub fn apply(&mut self, cmd: Command) -> CommandResponse {
        let resp = match cmd {
            Command::SubmitAllocation(alloc) => self.submit_allocation(alloc),
            Command::UpdateAllocationState {
                id,
                state,
                message,
                exit_code,
            } => self.update_allocation_state(id, state, message, exit_code),
            Command::AssignNodes {
                id,
                nodes,
                expected_version,
            } => self.assign_nodes(id, nodes, expected_version),
            Command::RequeueAllocation {
                id,
                expected_requeue_count,
            } => self.requeue_allocation(id, expected_requeue_count),
            Command::RegisterNode(node) => self.register_node(node),
            Command::UpdateNodeState { id, state, reason } => {
                self.update_node_state(id, state, reason)
            }
            Command::ClaimNode { id, ownership } => self.claim_node(id, ownership),
            Command::ReleaseNode { id } => self.release_node(id),
            Command::RecordHeartbeat {
                id,
                timestamp,
                owner_version,
                reattach_in_progress,
            } => self.record_heartbeat(id, timestamp, owner_version, reattach_in_progress),
            Command::UpdateNodeAddress {
                node_id,
                new_address,
            } => self.update_node_address(node_id, new_address),
            Command::ApplyCompletionReport {
                node_id,
                allocation_id,
                phase,
                pid,
                exit_code,
                reason,
            } => {
                self.apply_completion_report(node_id, allocation_id, phase, pid, exit_code, reason)
            }
            Command::RollbackDispatch {
                allocation_id,
                released_nodes,
                failed_node,
                observed_state_version,
                reason,
            } => self.rollback_dispatch(
                allocation_id,
                released_nodes,
                failed_node,
                observed_state_version,
                reason,
            ),
            Command::ProbeReleaseDegradedNode {
                node_id,
                expected_degraded_at,
            } => self.probe_release_degraded_node(node_id, expected_degraded_at),
            Command::CreateTenant(tenant) => self.create_tenant(tenant),
            Command::UpdateTenant {
                id,
                quota,
                isolation_level,
            } => self.update_tenant(id, quota, isolation_level),
            Command::CreateVCluster(vc) => self.create_vcluster(vc),
            Command::UpdateVCluster {
                id,
                cost_weights,
                allow_borrowing,
                allow_lending,
            } => self.update_vcluster(id, cost_weights, allow_borrowing, allow_lending),
            Command::UpdateTopology(topo) => {
                self.topology = topo;
                CommandResponse::Ok
            }
            Command::SetSensitivePoolSize(size) => {
                self.sensitive_pool_size = size;
                CommandResponse::Ok
            }
            Command::CreateNetworkDomain { tenant, name } => {
                self.create_network_domain(tenant, name)
            }
            Command::ReleaseNetworkDomain { tenant, name } => {
                self.release_network_domain(tenant, name)
            }
            Command::ResolveImage {
                id,
                image_index,
                resolved,
            } => self.resolve_image(id, image_index, resolved),
            Command::CreateSession(session) => self.create_session(session),
            Command::DeleteSession { session_id } => self.delete_session(session_id),
            Command::RecordAudit(entry) => self.record_audit(entry),
            Command::CompactAuditLog {
                object_key,
                entries_to_archive,
            } => self.compact_audit_log(object_key, entries_to_archive),
        };
        // Increment state version on every successful mutation (ADV-04).
        if !matches!(resp, CommandResponse::Error(_)) {
            self.state_version += 1;
        }
        resp
    }

    // ── Allocation operations ───────────────────────────────

    fn submit_allocation(&mut self, alloc: Allocation) -> CommandResponse {
        // Hard quota check
        if let Err(e) = self.check_hard_quota(&alloc) {
            return CommandResponse::Error(e.to_string());
        }

        let id = alloc.id;
        self.allocations.insert(id, alloc);
        CommandResponse::AllocationId(id)
    }

    fn update_allocation_state(
        &mut self,
        id: AllocId,
        new_state: AllocationState,
        message: Option<String>,
        exit_code: Option<i32>,
    ) -> CommandResponse {
        let Some(alloc) = self.allocations.get_mut(&id) else {
            return CommandResponse::Error(format!("Allocation not found: {id}"));
        };

        if !alloc.state.can_transition_to(&new_state) {
            return CommandResponse::Error(format!(
                "Invalid transition from {:?} to {:?}",
                alloc.state, new_state
            ));
        }

        alloc.state = new_state.clone();
        if let Some(msg) = message {
            alloc.message = Some(msg);
        }
        if let Some(code) = exit_code {
            alloc.exit_code = Some(code);
        }

        // Update timestamps
        match new_state {
            AllocationState::Running => {
                alloc.started_at = Some(Utc::now());
            }
            AllocationState::Completed | AllocationState::Failed | AllocationState::Cancelled => {
                alloc.completed_at = Some(Utc::now());
            }
            _ => {}
        }

        // Service endpoint registration/deregistration
        let expose = alloc.connectivity.expose.clone();
        let tenant = alloc.tenant.clone();
        let nodes = alloc.assigned_nodes.clone();
        match &alloc.state {
            AllocationState::Running if !expose.is_empty() => {
                for ep in &expose {
                    let entry = self.service_registry.entry(ep.name.clone()).or_default();
                    // Avoid duplicate registrations for the same allocation
                    if !entry.endpoints.iter().any(|e| e.allocation_id == id) {
                        entry.endpoints.push(RegisteredEndpoint {
                            allocation_id: id,
                            tenant: tenant.clone(),
                            nodes: nodes.clone(),
                            port: ep.port,
                            protocol: ep.protocol.clone(),
                        });
                    }
                }
            }
            s if s.is_terminal() && !expose.is_empty() => {
                for ep in &expose {
                    if let Some(entry) = self.service_registry.get_mut(&ep.name) {
                        entry.endpoints.retain(|e| e.allocation_id != id);
                        // Remove empty entries to keep registry clean
                    }
                }
                self.service_registry.retain(|_, v| !v.endpoints.is_empty());
            }
            _ => {}
        }

        CommandResponse::Ok
    }

    fn assign_nodes(
        &mut self,
        id: AllocId,
        nodes: Vec<NodeId>,
        expected_version: Option<u64>,
    ) -> CommandResponse {
        // Optimistic concurrency check (ADV-04).
        if let Some(ev) = expected_version {
            if ev != self.state_version {
                return CommandResponse::Error(format!(
                    "Stale proposal: expected state version {ev}, current is {}. Retry with fresh state.",
                    self.state_version
                ));
            }
        }
        let Some(alloc) = self.allocations.get(&id) else {
            return CommandResponse::Error(format!("Allocation not found: {id}"));
        };
        let tenant_id = alloc.tenant.clone();
        let new_node_count = nodes.len() as u32;

        // Re-check tenant max_nodes quota before assigning
        if let Some(tenant) = self.tenants.get(&tenant_id) {
            let other_nodes_in_use: u32 = self
                .allocations
                .iter()
                .filter(|(aid, a)| **aid != id && a.tenant == tenant_id && !a.state.is_terminal())
                .map(|(_, a)| a.assigned_nodes.len() as u32)
                .sum();

            if other_nodes_in_use + new_node_count > tenant.quota.max_nodes {
                return CommandResponse::Error(format!(
                    "Quota exceeded for tenant {}: max_nodes ({}) would be exceeded: \
                     {} in use by other allocations + {} to assign",
                    tenant_id, tenant.quota.max_nodes, other_nodes_in_use, new_node_count
                ));
            }
        }

        let Some(alloc) = self.allocations.get_mut(&id) else {
            return CommandResponse::Error(format!("Allocation vanished during assign: {id}"));
        };
        alloc.assigned_nodes = nodes;
        // D-ADV-IMPL-07 fix: record when the allocation was placed, so
        // the silent-sweep fresh-allocation exemption uses the correct
        // timestamp rather than the much-older submission timestamp.
        alloc.assigned_at = Some(Utc::now());
        CommandResponse::Ok
    }

    fn resolve_image(
        &mut self,
        id: AllocId,
        image_index: usize,
        resolved: lattice_common::types::ImageRef,
    ) -> CommandResponse {
        let Some(alloc) = self.allocations.get_mut(&id) else {
            return CommandResponse::Error(format!("Allocation not found: {id}"));
        };

        if image_index >= alloc.environment.images.len() {
            return CommandResponse::Error(format!(
                "Image index {image_index} out of bounds (allocation has {} images)",
                alloc.environment.images.len()
            ));
        }

        let img = &mut alloc.environment.images[image_index];
        img.sha256 = resolved.sha256;
        img.size_bytes = resolved.size_bytes;
        img.registry = resolved.registry;
        img.name = resolved.name;
        img.version = resolved.version;
        img.resolve_on_schedule = false;
        CommandResponse::Ok
    }

    fn requeue_allocation(
        &mut self,
        id: AllocId,
        expected_requeue_count: Option<u32>,
    ) -> CommandResponse {
        let Some(alloc) = self.allocations.get_mut(&id) else {
            return CommandResponse::Error(format!("Allocation not found: {id}"));
        };

        // F14: Optimistic concurrency — reject if count changed
        if let Some(expected) = expected_requeue_count {
            if alloc.requeue_count != expected {
                return CommandResponse::Error(format!(
                    "Requeue conflict: expected count {expected}, actual {}",
                    alloc.requeue_count
                ));
            }
        }

        if !alloc.state.can_transition_to(&AllocationState::Pending) {
            return CommandResponse::Error(format!("Cannot requeue from {:?}", alloc.state));
        }

        if alloc.requeue_count >= alloc.max_requeue {
            return CommandResponse::Error(format!(
                "Max requeue limit reached ({}/{})",
                alloc.requeue_count, alloc.max_requeue
            ));
        }

        // Deregister service endpoints before resetting state
        let expose = alloc.connectivity.expose.clone();
        for ep in &expose {
            if let Some(entry) = self.service_registry.get_mut(&ep.name) {
                entry.endpoints.retain(|e| e.allocation_id != id);
            }
        }
        self.service_registry.retain(|_, v| !v.endpoints.is_empty());

        alloc.state = AllocationState::Pending;
        alloc.requeue_count += 1;
        alloc.assigned_nodes.clear();
        alloc.started_at = None;
        alloc.completed_at = None;
        alloc.exit_code = None;
        alloc.message = None;
        // INT-2: clear dispatch-specific fields so the next placement sees a
        // clean slate. Without this, a requeue carries stale per_node_phase
        // into its next dispatch (ApplyCompletionReport would aggregate over
        // OLD nodes + the newly assigned ones), assigned_at would still
        // point at the previous placement time (silent-sweep fresh-exemption
        // would be wrong), dispatch_retry_count would accumulate across
        // requeues (a long-running service with occasional node failures
        // would eventually hit max_dispatch_retries and go to Failed
        // permanently), and last_completion_report_at would make the
        // Dispatcher think the new placement has already been ack'd.
        alloc.per_node_phase.clear();
        alloc.assigned_at = None;
        alloc.dispatch_retry_count = 0;
        alloc.last_completion_report_at = None;
        CommandResponse::Ok
    }

    // ── Session operations (F20) ──────────────────────────────

    fn create_session(&mut self, session: Session) -> CommandResponse {
        let alloc_id = session.allocation_id;

        // Verify allocation exists and is Running
        let Some(alloc) = self.allocations.get(&alloc_id) else {
            return CommandResponse::Error(format!("Allocation not found: {alloc_id}"));
        };
        if alloc.state != AllocationState::Running {
            return CommandResponse::Error(format!(
                "Allocation {alloc_id} is {:?}, not Running",
                alloc.state
            ));
        }

        // Enforce global session limit for sensitive allocations (INV-C2)
        if alloc.sensitive {
            let existing = self
                .sessions
                .values()
                .filter(|s| s.allocation_id == alloc_id)
                .count();
            if existing >= 1 {
                return CommandResponse::Error(format!(
                    "Sensitive allocation {alloc_id} already has an active session (INV-C2)"
                ));
            }
        }

        let id = session.id;
        self.sessions.insert(id, session);
        CommandResponse::SessionId(id)
    }

    fn delete_session(&mut self, session_id: SessionId) -> CommandResponse {
        if self.sessions.remove(&session_id).is_none() {
            return CommandResponse::Error(format!("Session not found: {session_id}"));
        }
        CommandResponse::Ok
    }

    // ── Node operations ─────────────────────────────────────

    fn register_node(&mut self, node: Node) -> CommandResponse {
        let id = node.id.clone();
        self.nodes.insert(id.clone(), node);
        CommandResponse::NodeId(id)
    }

    fn update_node_state(
        &mut self,
        id: NodeId,
        new_state: NodeState,
        reason: Option<String>,
    ) -> CommandResponse {
        let Some(node) = self.nodes.get_mut(&id) else {
            return CommandResponse::Error(format!("Node not found: {id}"));
        };

        if !node.state.can_transition_to(&new_state) {
            return CommandResponse::Error(format!(
                "Invalid node transition from {:?} to {:?}",
                node.state, new_state
            ));
        }

        node.state = new_state;
        let _ = reason; // Could store in a reason field if needed
        CommandResponse::Ok
    }

    fn claim_node(&mut self, id: NodeId, ownership: NodeOwnership) -> CommandResponse {
        // Check node exists and ownership conflict (immutable borrows first)
        {
            let Some(node) = self.nodes.get(&id) else {
                return CommandResponse::Error(format!("Node not found: {id}"));
            };
            if let Some(ref existing) = node.owner {
                if existing.claimed_by.is_some() && ownership.claimed_by.is_some() {
                    return CommandResponse::Error(format!(
                        "Node {id} already claimed by {}",
                        existing.claimed_by.as_deref().unwrap_or("unknown")
                    ));
                }
            }
        }

        // Check sensitive_pool_size limit if this is a sensitive claim
        if ownership.claimed_by.is_some() {
            if let Some(pool_limit) = self.sensitive_pool_size {
                let currently_claimed = self
                    .nodes
                    .values()
                    .filter(|n| {
                        n.owner
                            .as_ref()
                            .map(|o| o.claimed_by.is_some())
                            .unwrap_or(false)
                    })
                    .count() as u32;

                if currently_claimed >= pool_limit {
                    return CommandResponse::Error(format!(
                        "sensitive_pool_size limit ({}) reached: {} nodes already claimed",
                        pool_limit, currently_claimed
                    ));
                }
            }
        }

        let Some(node) = self.nodes.get_mut(&id) else {
            return CommandResponse::Error(format!("Node vanished during claim: {id}"));
        };
        node.owner = Some(ownership);
        node.owner_version += 1; // ADV-06: bump on ownership change
        CommandResponse::Ok
    }

    fn release_node(&mut self, id: NodeId) -> CommandResponse {
        let Some(node) = self.nodes.get_mut(&id) else {
            return CommandResponse::Error(format!("Node not found: {id}"));
        };
        node.owner = None;
        node.owner_version += 1; // ADV-06: bump on ownership change
        CommandResponse::Ok
    }

    fn record_heartbeat(
        &mut self,
        id: NodeId,
        timestamp: DateTime<Utc>,
        owner_version: u64,
        reattach_in_progress: bool,
    ) -> CommandResponse {
        let Some(node) = self.nodes.get_mut(&id) else {
            return CommandResponse::Error(format!("Node not found: {id}"));
        };
        // ADV-06: reject heartbeats from stale owner contexts.
        if owner_version != node.owner_version {
            return CommandResponse::Error(format!(
                "Stale heartbeat for node {id}: owner_version {owner_version} != current {}",
                node.owner_version
            ));
        }
        node.last_heartbeat = Some(timestamp);

        // INV-D5 flag lifecycle (DEC-DISP-03 / DEC-DISP-10 / D-ADV-ARCH-06):
        //   1. The flag may only transition false → true on the first
        //      heartbeat following a fresh RegisterNode / UpdateNodeAddress
        //      (we cannot observe that here, but the first true-valued
        //      heartbeat since the node had no prior first_set_at is the
        //      proxy). Once set, reattach_first_set_at is the absolute
        //      grace-window start.
        //   2. Subsequent true-valued heartbeats during the grace window do
        //      NOT reset the timer.
        //   3. Set to false is one-way within a registration lifetime: once
        //      cleared, the flag cannot return to true without a fresh
        //      RegisterNode (which resets first_set_at).
        //   4. After `reattach_grace_period` has elapsed since
        //      reattach_first_set_at, INV-D8 treats the flag as false
        //      regardless of its recorded value. Enforcement of that
        //      deadline lives in the silent-sweep consumer, not here.
        match (node.reattach_in_progress, reattach_in_progress) {
            (false, true) => {
                if node.reattach_first_set_at.is_none() {
                    node.reattach_in_progress = true;
                    node.reattach_first_set_at = Some(timestamp);
                }
                // Rule 3: once cleared, cannot return to true.
            }
            (true, true) => {
                // No-op: flag already set; do not reset timer.
            }
            (true, false) => {
                // One-way clear: leave first_set_at in place so the
                // lifecycle remains observable.
                node.reattach_in_progress = false;
            }
            (false, false) => {
                // No-op.
            }
        }

        CommandResponse::Ok
    }

    // ── Dispatch command handlers (INV-D1, INV-D4/D6/D7/D11/D12) ────────

    fn update_node_address(&mut self, node_id: NodeId, new_address: String) -> CommandResponse {
        // Syntactic invariants are checked at the API layer (INV-D1); this
        // apply-step re-validates that the address is non-empty as a
        // defense-in-depth against crafted proposals.
        if new_address.trim().is_empty() {
            return CommandResponse::Error("agent_address_required".into());
        }
        let Some(node) = self.nodes.get_mut(&node_id) else {
            return CommandResponse::Error(format!("Node not found: {node_id}"));
        };
        if node.agent_address == new_address {
            return CommandResponse::Ok;
        }
        node.agent_address = new_address;
        // Re-registration semantics: a new address is observable like a
        // fresh registration, so reset the reattach lifecycle tracker.
        node.reattach_in_progress = false;
        node.reattach_first_set_at = None;
        CommandResponse::Ok
    }

    fn apply_completion_report(
        &mut self,
        node_id: NodeId,
        allocation_id: AllocId,
        phase: CompletionPhase,
        pid: Option<u32>,
        exit_code: Option<i32>,
        reason: Option<String>,
    ) -> CommandResponse {
        let Some(alloc) = self.allocations.get_mut(&allocation_id) else {
            return CommandResponse::Error(format!("Allocation not found: {allocation_id}"));
        };

        // INV-D12 source-auth: reporting node must be assigned to this allocation.
        if !alloc.assigned_nodes.contains(&node_id) {
            lattice_common::metrics::record_cross_node_report(&node_id);
            return CommandResponse::Error(format!(
                "cross_node_report: {node_id} not in assigned_nodes for {allocation_id}"
            ));
        }

        // INV-D7 monotonicity + INV-D4 idempotency are applied at the
        // per-node granularity below, because DEC-DISP-11 requires per-node
        // tracking: aggregate `alloc.state` is a derived function of
        // `per_node_phase`, not an independent variable. The aggregate
        // cannot regress under the rules in use without a separate cancel
        // or rollback command.
        //
        // Per-node phase tracking (DEC-DISP-11 conservative aggregation).
        // Apply INV-D7 monotonicity at the per-node level, not just the
        // aggregated level: a node's own phase must advance monotonically,
        // else reject. Fix for D-ADV-IMPL-06.
        if let Some(&prev_phase) = alloc.per_node_phase.get(&node_id) {
            if phase_rank(phase) < phase_rank(prev_phase) {
                lattice_common::metrics::record_phase_regression(
                    &format!("{prev_phase:?}"),
                    &format!("{phase:?}"),
                );
                return CommandResponse::Error(format!(
                    "phase_regression_per_node: {node_id} current {prev_phase:?} vs reported {phase:?}"
                ));
            }
            if phase_rank(phase) == phase_rank(prev_phase) {
                // INV-D4 idempotency at per-node granularity.
                return CommandResponse::Ok;
            }
        }
        alloc.per_node_phase.insert(node_id.clone(), phase);

        // DEC-DISP-11 aggregation:
        //   Staging  : any assigned node is Staging (or no report yet).
        //   Running  : ALL assigned nodes are Running or beyond.
        //   Completed: ALL assigned nodes are Completed with exit 0.
        //   Failed   : ANY assigned node is Failed, OR ALL are Completed
        //              but at least one had non-zero exit_code.
        let per_node = &alloc.per_node_phase;
        let all_assigned: &[NodeId] = &alloc.assigned_nodes;
        let any_failed = all_assigned
            .iter()
            .any(|n| per_node.get(n) == Some(&CompletionPhase::Failed));
        let all_have_report = all_assigned.iter().all(|n| per_node.contains_key(n));
        let all_running_plus = all_have_report
            && all_assigned.iter().all(|n| {
                matches!(
                    per_node.get(n),
                    Some(CompletionPhase::Running)
                        | Some(CompletionPhase::Completed)
                        | Some(CompletionPhase::Failed),
                )
            });
        let all_completed = all_have_report
            && all_assigned
                .iter()
                .all(|n| matches!(per_node.get(n), Some(CompletionPhase::Completed)));

        let exit_nonzero = exit_code.is_some() && exit_code != Some(0);
        let new_state = if any_failed {
            AllocationState::Failed
        } else if all_completed {
            if exit_nonzero {
                AllocationState::Failed
            } else {
                AllocationState::Completed
            }
        } else if all_running_plus {
            AllocationState::Running
        } else {
            AllocationState::Staging
        };
        alloc.state = new_state;
        alloc.last_completion_report_at = Some(Utc::now());
        if let Some(code) = exit_code {
            alloc.exit_code = Some(code);
        }
        if reason.is_some() {
            alloc.message = reason;
        }
        if pid.is_some() {
            // PID is agent-local; not surfaced in the quorum state machine
            // beyond acknowledging the transition.
        }
        match phase {
            CompletionPhase::Completed | CompletionPhase::Failed => {
                alloc.completed_at = Some(Utc::now());
            }
            CompletionPhase::Running if alloc.started_at.is_none() => {
                alloc.started_at = Some(Utc::now());
            }
            _ => {}
        }
        alloc.state_version += 1;

        // INV-D11 reset: a successful Staging/Running report is evidence
        // that dispatch worked on this node, so clear its failure counter.
        if matches!(phase, CompletionPhase::Staging | CompletionPhase::Running) {
            if let Some(node) = self.nodes.get_mut(&node_id) {
                node.consecutive_dispatch_failures = 0;
            }
        }

        CommandResponse::Ok
    }

    fn rollback_dispatch(
        &mut self,
        allocation_id: AllocId,
        released_nodes: Vec<NodeId>,
        failed_node: Option<NodeId>,
        observed_state_version: u64,
        reason: String,
    ) -> CommandResponse {
        let Some(alloc) = self.allocations.get_mut(&allocation_id) else {
            return CommandResponse::Error(format!("Allocation not found: {allocation_id}"));
        };

        // INV-D6 optimistic concurrency: reject if the allocation has
        // advanced since the dispatcher's observation.
        if alloc.state_version != observed_state_version {
            return CommandResponse::Error(format!(
                "state_version_mismatch: observed {observed_state_version}, current {}",
                alloc.state_version
            ));
        }

        const MAX_DISPATCH_RETRIES: u32 = 3;

        // Release all assigned nodes (DEC-DISP-07 all-or-nothing).
        for node_id in &released_nodes {
            if let Some(node) = self.nodes.get_mut(node_id) {
                if node.owner.as_ref().map(|o| o.allocation) == Some(allocation_id) {
                    node.owner = None;
                    node.owner_version += 1;
                }
            }
        }

        // Clear allocation's assigned_nodes, per-node phase tracking,
        // and increment retry counter. Per-node phase must be cleared so
        // the next dispatch attempt starts fresh aggregation.
        alloc.assigned_nodes.clear();
        alloc.per_node_phase.clear();
        alloc.assigned_at = None;
        alloc.dispatch_retry_count += 1;
        alloc.message = Some(reason);

        // Cap check: move to Failed if we've exhausted the retry budget,
        // otherwise return to Pending for re-placement.
        if alloc.dispatch_retry_count >= MAX_DISPATCH_RETRIES {
            alloc.state = AllocationState::Failed;
            alloc.completed_at = Some(Utc::now());
        } else {
            alloc.state = AllocationState::Pending;
            alloc.started_at = None;
        }
        alloc.state_version += 1;

        // INV-D11: credit the specific node whose dispatch failed.
        if let Some(node_id) = failed_node {
            if let Some(node) = self.nodes.get_mut(&node_id) {
                node.consecutive_dispatch_failures =
                    node.consecutive_dispatch_failures.saturating_add(1);
                // Degraded transition is subject to the cluster-wide guard
                // (DEC-DISP-01); simplified here: transition when the
                // node-level cap is reached.
                const MAX_NODE_DISPATCH_FAILURES: u32 = 5;
                if node.consecutive_dispatch_failures >= MAX_NODE_DISPATCH_FAILURES
                    && matches!(node.state, NodeState::Ready)
                {
                    lattice_common::metrics::record_node_degraded(&node_id);
                    node.state = NodeState::Degraded {
                        reason: "max_node_dispatch_failures".into(),
                    };
                    node.degraded_at = Some(Utc::now());
                }
            }
        }

        CommandResponse::Ok
    }

    fn probe_release_degraded_node(
        &mut self,
        node_id: NodeId,
        expected_degraded_at: DateTime<Utc>,
    ) -> CommandResponse {
        let Some(node) = self.nodes.get_mut(&node_id) else {
            return CommandResponse::Error(format!("Node not found: {node_id}"));
        };
        // Version check: bail if the node has been degraded again since
        // the dispatcher observed the prior degraded_at timestamp.
        if node.degraded_at != Some(expected_degraded_at) {
            return CommandResponse::Error(format!(
                "degraded_at_mismatch: observed {expected_degraded_at}, current {:?}",
                node.degraded_at
            ));
        }
        if !matches!(node.state, NodeState::Degraded { .. }) {
            return CommandResponse::Error(format!("Node {node_id} is not Degraded"));
        }
        node.consecutive_dispatch_failures /= 2;
        node.state = NodeState::Ready;
        node.degraded_at = None;
        CommandResponse::Ok
    }

