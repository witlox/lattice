    fn audit_signature_verification() {
        let mut state = GlobalState::new();
        let entry = AuditEntry::new(lattice_audit_event(
            audit_actions::DATA_ACCESS,
            "user-1",
            hpc_audit::AuditScope::default(),
            hpc_audit::AuditOutcome::Success,
            "data access",
            serde_json::json!({"file": "/data/sensitive.csv"}),
            hpc_audit::AuditSource::LatticeQuorum,
        ));
        state.apply(Command::RecordAudit(entry));

        let vk = state.verifying_key();
        // Valid signature passes
        assert!(GlobalState::verify_audit_entry(&state.audit_log[0], &vk));

        // Tamper with the entry and verify it fails
        let mut tampered = state.audit_log[0].clone();
        tampered.event.principal.identity = "attacker".into();
        assert!(!GlobalState::verify_audit_entry(&tampered, &vk));

        // Invalid hex signature fails gracefully
        let mut bad_sig = state.audit_log[0].clone();
        bad_sig.signature = "not-valid-hex!".into();
        assert!(!GlobalState::verify_audit_entry(&bad_sig, &vk));

        // Wrong-length signature fails gracefully
        let mut short_sig = state.audit_log[0].clone();
        short_sig.signature = "abcd".into();
        assert!(!GlobalState::verify_audit_entry(&short_sig, &vk));

        // Verify with a different key fails
        let other_key = SigningKey::generate(&mut rand_core::OsRng);
        let other_vk = other_key.verifying_key();
        assert!(!GlobalState::verify_audit_entry(
            &state.audit_log[0],
            &other_vk
        ));
    }

