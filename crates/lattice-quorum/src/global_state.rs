impl GlobalState {
    // ── Query helpers ───────────────────────────────────────

    pub fn query_audit(&self, filter: &AuditFilter) -> Vec<AuditEntry> {
        self.audit_log
            .iter()
            .filter(|e| {
                if let Some(ref principal) = filter.principal {
                    if &e.event.principal.identity != principal {
                        return false;
                    }
                }
                if let Some(ref action) = filter.action {
                    if &e.event.action != action {
                        return false;
                    }
                }
                if let Some(ref alloc) = filter.allocation {
                    let alloc_str = alloc.to_string();
                    if e.event.scope.allocation_id.as_deref() != Some(alloc_str.as_str()) {
                        return false;
                    }
                }
                if let Some(ref since) = filter.since {
                    if e.event.timestamp < *since {
                        return false;
                    }
                }
                if let Some(ref until) = filter.until {
                    if e.event.timestamp > *until {
                        return false;
                    }
                }
                true
            })
            .cloned()
            .collect()
    }

    /// Look up registered endpoints for a named service.
    pub fn lookup_service(&self, name: &str) -> Option<&ServiceRegistryEntry> {
        self.service_registry.get(name)
    }

    /// List all registered service names.
    pub fn list_services(&self) -> Vec<String> {
        self.service_registry.keys().cloned().collect()
    }
}

